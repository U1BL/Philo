#include "libft.h"

int	ft_isdigit(const char *str, char c, int *i)
{
	if (!str)
		return (0);
	if ((c >= '0' && c <= '9') || (str[0] == '-'
			&& (*i) == 0 && str[(*i) + 1] >= '0'
			&& str[(*i) + 1] <= '9') || \
				(str[0] == '+' && (*i) == 0 && str[(*i) + 1] >= '0'
			&& str[(*i) + 1] <= '9'))
		return (1);
	return (0);
}
